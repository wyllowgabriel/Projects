"Ejemplo para break"
print("While with the sentence break. \n")
contador = 0
while contador < 10:
    contador += 1 

    if contador == 5:
        break
    print("Current value of the variable: ", contador)

"Ejemplo para continue"

print("\nWhile with the sentence continue. \n")
contador = 0
while contador < 10:
    contador += 1 

    if contador == 5:
        continue
    
    print("Current value of the variable: ", contador)
